package com.colt.ws.biz;

public class ASideInformation {

	private String port;
	private String vendor;
	private String model;
	private String type;
	private String deviceName;
	private String xngDeviceName;
	private String instId;

	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getVendor() {
		return vendor;
	}
	public void setVendor(String vendor) {
		this.vendor = vendor;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	public String getXngDeviceName() {
		return xngDeviceName;
	}
	public void setXngDeviceName(String xngDeviceName) {
		this.xngDeviceName = xngDeviceName;
	}
	public String getInstId() {
		return instId;
	}
	public void setInstId(String instId) {
		this.instId = instId;
	}
}
