package com.colt.ws.biz;

public class ZSideInformation {

	private String port;
	private String type;
	private String vendor;
	private String xngDeviceName;
	private String xngNetworkObjectName;
	private String xngSlotNumber;
	private String model;
	private String deviceName;
	private String instId;
	private String ip;

	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getVendor() {
		return vendor;
	}
	public void setVendor(String vendor) {
		this.vendor = vendor;
	}
	public String getXngDeviceName() {
		return xngDeviceName;
	}
	public void setXngDeviceName(String xngDeviceName) {
		this.xngDeviceName = xngDeviceName;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getDeviceName() {
		return deviceName;
	}
	public void setDeviceName(String deviceName) {
		this.deviceName = deviceName;
	}
	public String getInstId() {
		return instId;
	}
	public void setInstId(String instId) {
		this.instId = instId;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getXngNetworkObjectName() {
		return xngNetworkObjectName;
	}
	public void setXngNetworkObjectName(String xngNetworkObjectName) {
		this.xngNetworkObjectName = xngNetworkObjectName;
	}
	public String getXngSlotNumber() {
		return xngSlotNumber;
	}
	public void setXngSlotNumber(String xngSlotNumber) {
		this.xngSlotNumber = xngSlotNumber;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
}
